const fs = require('node:fs');
const path = require('node:path');

const DEFAULT_SOURCE_TYPE = 'imported';
const DEFAULT_REVIEW_STATUS = 1;
const SQL_CURRENT_TIMESTAMP = 'NOW(3)';

function slugify(value) {
  const normalized = String(value)
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

  if (normalized) {
    return normalized;
  }

  return String(value)
    .trim()
    .split('')
    .map((char) => `u${char.codePointAt(0).toString(16)}`)
    .join('-');
}

function escapeSql(value) {
  return String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function sqlString(value) {
  return `'${escapeSql(value)}'`;
}

function optionalSqlString(value) {
  if (value === undefined || value === null || value === '') {
    return 'NULL';
  }

  return sqlString(value);
}

function loadQuestions(filePath) {
  const resolvedPath = path.resolve(filePath);
  const raw = fs.readFileSync(resolvedPath, 'utf8');
  const parsed = JSON.parse(raw);

  if (!Array.isArray(parsed)) {
    throw new Error('题库 JSON 顶层必须是数组。');
  }

  return parsed.map((item, index) => normalizeQuestion(item, index));
}

function normalizeQuestion(item, index) {
  const displayIndex = index + 1;
  const requiredFields = ['title', 'content', 'answer', 'difficulty', 'category', 'tags'];

  for (const field of requiredFields) {
    if (!(field in item)) {
      throw new Error(`第 ${displayIndex} 条题目缺少字段: ${field}`);
    }
  }

  if (!Array.isArray(item.tags)) {
    throw new Error(`第 ${displayIndex} 条题目的 tags 必须是数组。`);
  }

  const difficulty = String(item.difficulty).trim();
  const validDifficulties = new Set(['easy', 'medium', 'hard']);

  if (!validDifficulties.has(difficulty)) {
    throw new Error(`第 ${displayIndex} 条题目的 difficulty 非法: ${difficulty}`);
  }

  const title = String(item.title).trim();
  const content = String(item.content).trim();
  const answer = String(item.answer).trim();
  const categoryName = String(item.category).trim();
  const categorySlug = item.categorySlug ? String(item.categorySlug).trim() : slugify(categoryName);
  const tags = item.tags
    .map((tag) => String(tag).trim())
    .filter(Boolean)
    .map((tagName) => ({ name: tagName, slug: slugify(tagName) }));

  if (!title || !content || !answer || !categoryName || !categorySlug || tags.length === 0) {
    throw new Error(`第 ${displayIndex} 条题目存在空字段，请检查 title/content/answer/category/tags。`);
  }

  return {
    title,
    content,
    answer,
    difficulty,
    categoryName,
    categorySlug,
    tags,
    sourceName: item.sourceName ? String(item.sourceName).trim() : null,
    sourceUrl: item.sourceUrl ? String(item.sourceUrl).trim() : null,
  };
}

function buildSql(questions) {
  const categories = new Map();
  const tags = new Map();

  for (const question of questions) {
    categories.set(question.categorySlug, question.categoryName);
    for (const tag of question.tags) {
      tags.set(tag.slug, tag.name);
    }
  }

  const lines = [
    '-- Auto-generated by generate-question-import-sql.cjs',
    'START TRANSACTION;',
    '',
    '-- categories',
  ];

  for (const [slug, name] of categories.entries()) {
    lines.push(
      `INSERT INTO categories (name, slug, created_at, updated_at) VALUES (${sqlString(name)}, ${sqlString(slug)}, ${SQL_CURRENT_TIMESTAMP}, ${SQL_CURRENT_TIMESTAMP}) ON DUPLICATE KEY UPDATE name = VALUES(name), updated_at = VALUES(updated_at);`,
    );
  }

  lines.push('', '-- tags');

  for (const [slug, name] of tags.entries()) {
    lines.push(
      `INSERT INTO tags (name, slug, created_at, updated_at) VALUES (${sqlString(name)}, ${sqlString(slug)}, ${SQL_CURRENT_TIMESTAMP}, ${SQL_CURRENT_TIMESTAMP}) ON DUPLICATE KEY UPDATE name = VALUES(name), updated_at = VALUES(updated_at);`,
    );
  }

  lines.push('', '-- questions');

  questions.forEach((question, index) => {
    const sourceUrlSql = optionalSqlString(question.sourceUrl);
    lines.push(`-- question ${index + 1}: ${question.title}`);
    lines.push(
      `INSERT INTO questions (title, content, answer, difficulty, category_id, source_type, source_url, source_name, review_status, published_at, created_at, updated_at)` +
        ` SELECT ${sqlString(question.title)}, ${sqlString(question.content)}, ${sqlString(question.answer)}, ${sqlString(question.difficulty)}, c.id, ${sqlString(DEFAULT_SOURCE_TYPE)}, ${sourceUrlSql}, ${optionalSqlString(question.sourceName)}, ${DEFAULT_REVIEW_STATUS}, ${SQL_CURRENT_TIMESTAMP}, ${SQL_CURRENT_TIMESTAMP}, ${SQL_CURRENT_TIMESTAMP}` +
        ` FROM categories c WHERE c.slug = ${sqlString(question.categorySlug)}` +
        ` AND NOT EXISTS (` +
        `SELECT 1 FROM questions q WHERE q.title = ${sqlString(question.title)} AND q.source_url <=> ${sourceUrlSql}` +
        `);`,
    );
    lines.push(
      `SET @question_id = (` +
        `SELECT id FROM questions WHERE title = ${sqlString(question.title)} AND source_url <=> ${sourceUrlSql} ORDER BY id DESC LIMIT 1` +
        `);`,
    );

    for (const tag of question.tags) {
      lines.push(
        `INSERT IGNORE INTO question_tags (question_id, tag_id) SELECT @question_id, t.id FROM tags t WHERE t.slug = ${sqlString(tag.slug)};`,
      );
    }

    lines.push('');
  });

  lines.push('COMMIT;', '');
  return lines.join('\n');
}

function main() {
  const [, , inputPath = './question-import.template.json', outputPath = './question-import.sql'] = process.argv;
  const questions = loadQuestions(inputPath);
  const sql = buildSql(questions);
  const resolvedOutputPath = path.resolve(outputPath);

  fs.writeFileSync(resolvedOutputPath, sql, 'utf8');
  process.stdout.write(`已生成 SQL: ${resolvedOutputPath}\n题目数量: ${questions.length}\n`);
}

main();
